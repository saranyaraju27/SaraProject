package week1.day2;

public class SwitchCase {
	public static void main(String[] args) {
		
		//multiplecases
		int number=0;
		switch(number) {
		case 0:
			System.out.println("The number is zero");
			break;
		case 1:
			System.out.println("Greater than zero");
			break;	
			default:
				System.out.println("test");
		}			
		
		
		}

	}


